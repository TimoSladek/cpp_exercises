#include<bits/stdc++.h>

using namespace std;

int main() {
    int n;
    cin >> n;
    if (n == 1)cout << "ABD" << endl;
    if (n == 2)cout << "ACD" << endl;
    if (n == 3)cout << "ACCCCCD" << endl;
    if (n == 4)cout << "AACDD" << endl;
    if (n == 5)cout << "AACCCCCDCABDABDABDABDABDD" << endl;
    if (n == 6)cout << "AAACCCCCDCABDABDABDABDABDDCBBBBBAACDDAACDDAACDDAACDDAACDDD" << endl;
    return 0;
}